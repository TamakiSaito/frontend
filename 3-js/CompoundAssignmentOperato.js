// 累算代入演算子

// 足し算 +=
let a = 5;
a += 3;  // a = a + 3; なので結果は a = 8


// 引き算 -=
let b = 10;
b -= 4;  // b = b - 4; なので結果は b = 6


// 掛け算 *=
let c = 7;
c *= 2;  // c = c * 2; なので結果は c = 14


// 割り算 /=
let d = 20;
d /= 4;  // d = d / 4; なので結果は d = 5


// 剰余（余り）%=
let e = 13;
e %= 5;  // e = e % 5; なので結果は e = 3


// 累乗 **=
let f = 3;
f **= 3; // f = f ** 3; なので結果は f = 27（3の3乗）